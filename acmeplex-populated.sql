CREATE TRIGGER `after_showtime_insert` AFTER INSERT ON `showtime` FOR EACH ROW BEGIN
    DECLARE seat_id INT;
    DECLARE done INT DEFAULT 0;
    DECLARE cursor_seat CURSOR FOR 
        SELECT `id` FROM `seat` WHERE `theatre_id` = NEW.theatre_id;

    DECLARE CONTINUE HANDLER FOR NOT FOUND SET done = 1;

    OPEN cursor_seat;

    seat_loop: LOOP
        FETCH cursor_seat INTO seat_id;

        IF done = 1 THEN
            LEAVE seat_loop;
        END IF;

        -- Insert into `showtime_seat`
        INSERT INTO `showtime_seat` (`showtime_id`, `seat_id`, `available`)
        VALUES (NEW.id, seat_id, 1);

        -- Insert into `ticket`
        INSERT INTO `ticket` (
            `id`,
            `ticket_number`,
            `holder_email`,
            `price`,
            `movie_id`,
            `theatre_id`,
            `showtime_id`,
            `seat_id`,
            `reserved_time`,
            `expire_time`
        ) VALUES (
            LAST_INSERT_ID(), -- ticket ID matches showtime_seat ID
            UPPER(CONCAT(SUBSTRING(MD5(RAND()), 1, 8))), -- Generate 8-character alphanumeric ticket number
            NULL, -- No holder initially
            NEW.ticket_price, -- Ticket price from showtime
            NEW.movie_id, -- Movie ID from showtime
            NEW.theatre_id, -- Theatre ID from showtime
            NEW.id, -- Showtime ID
            seat_id, -- Seat ID
            NULL, -- Reserved time is NULL
            NEW.end_time -- Expire time is the end time of the showtime
        );
    END LOOP;

    CLOSE cursor_seat;
END
